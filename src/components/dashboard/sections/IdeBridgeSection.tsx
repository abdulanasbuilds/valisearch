/* ─── File card ──────────────────────────────────────────── */
function FileCard({ spec, analysis }: { spec: FileSpec; analysis: ValiSearchAnalysis }) {
  const [copied, setCopied] = useState(false);
  const [expanded, setExpanded] = useState(false);
  const content = spec.generate(analysis);

  const copy = () => {
    navigator.clipboard.writeText(content).catch(() => {});
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const download = () => {
    const blob = new Blob([content], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = spec.filename;
    a.click();
    URL.revokeObjectURL(url);
  };

  const BADGE_STYLE: Record<string, string> = {
    cursor: "text-violet-400 bg-violet-500/10 border-violet-500/20",
    vscode: "text-blue-400 bg-blue-500/10 border-blue-500/20",
    nocode: "text-green-400 bg-green-500/10 border-green-500/20",
  };

  const BADGE_LABEL: Record<string, string> = {
    cursor: "Cursor AI",
    vscode: "VS Code",
    nocode: "No-Code",
  };

  return (
    <div className="rounded-xl border border-white/[0.07] bg-white/[0.02] overflow-hidden">
      <div className="flex items-start gap-3 p-4">
        <div className="flex-1 min-w-0">
          <div className="flex items-center gap-2 mb-1">
            <code className="text-[13px] font-bold text-white/80 font-mono">{spec.label}</code>
            <span className={`text-[9px] font-semibold uppercase tracking-wider px-1.5 py-0.5 rounded-full border ${BADGE_STYLE[spec.category]}`}>
              {BADGE_LABEL[spec.category]}
            </span>
          </div>
          <p className="text-[12px] text-muted-foreground leading-snug">{spec.description}</p>
          <div className="text-[10.5px] text-white/20 font-mono mt-1">{spec.filename}</div>
        </div>
        <div className="flex items-center gap-1.5 shrink-0">
          <button
            onClick={() => setExpanded((x) => !x)}
            className="p-1.5 rounded-lg text-white/30 hover:text-white/70 hover:bg-white/[0.06] transition-colors"
            title="Preview"
          >
            {expanded ? <ChevronUp className="h-4 w-4" /> : <ChevronDown className="h-4 w-4" />}
          </button>
          <button
            onClick={copy}
            className="p-1.5 rounded-lg text-white/30 hover:text-white/70 hover:bg-white/[0.06] transition-colors"
            title="Copy"
          >
            {copied ? <Check className="h-4 w-4 text-green-400" /> : <Copy className="h-4 w-4" />}
          </button>
          <button
            onClick={download}
            className="p-1.5 rounded-lg text-white/30 hover:text-white/70 hover:bg-white/[0.06] transition-colors"
            title="Download"
          >
            <Download className="h-4 w-4" />
          </button>
        </div>
      </div>

      {expanded && (
        <div className="border-t border-white/[0.06] max-h-72 overflow-y-auto">
          <pre className="p-4 text-[11px] text-white/50 font-mono leading-relaxed whitespace-pre-wrap break-words">
            {content}
          </pre>
        </div>
      )}
    </div>
  );
}

/* ─── Main ───────────────────────────────────────────────── */
export function IdeBridgeSection() {
  const analysis = useAnalysisStore((s) => s.analysis);
  if (!analysis) return null;

  const downloadAll = () => {
    FILES.forEach((spec, i) => {
      setTimeout(() => {
        const content = spec.generate(analysis);
        const blob = new Blob([content], { type: "text/plain" });
        const url = URL.createObjectURL(blob);
        const a = document.createElement("a");
        a.href = url;
        a.download = spec.filename;
        a.click();
        URL.revokeObjectURL(url);
      }, i * 350);
    });
  };

  const categories = [
    { key: "cursor", label: "Cursor AI", subtitle: "AI-native IDE with codebase understanding" },
    { key: "vscode", label: "VS Code", subtitle: "Workspace settings and task runner config" },
    { key: "nocode", label: "No-Code Builders", subtitle: "Ready-to-paste prompts for Lovable & Bolt" },
  ] as const;

  return (
    <div className="space-y-6 max-w-4xl">
      <div className="flex items-start justify-between flex-wrap gap-3">
        <div>
          <h2 className="text-xl font-bold tracking-tight">IDE Bridge</h2>
          <p className="text-[13px] text-muted-foreground mt-1">
            Export your project spec to Cursor, VS Code, Lovable, or Bolt.new — ready to paste and build
          </p>
        </div>
        <Button
          size="sm"
          variant="outline"
          className="gap-2 h-8 text-[12px]"
          onClick={downloadAll}
        >
          <Download className="h-3.5 w-3.5" /> Download All ({FILES.length} files)
        </Button>
      </div>

      {categories.map(({ key, label, subtitle }) => (
        <div key={key} className="space-y-3">
          <div>
            <div className="text-[13px] font-semibold text-white/70">{label}</div>
            <div className="text-[11px] text-muted-foreground/50 mt-0.5">{subtitle}</div>
          </div>
          <div className="space-y-2">
            {FILES.filter((f) => f.category === key).map((spec) => (
              <FileCard key={spec.key} spec={spec} analysis={analysis} />
            ))}
          </div>
        </div>
      ))}
    </div>
  );
}
