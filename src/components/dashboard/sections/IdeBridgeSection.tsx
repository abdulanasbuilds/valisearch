/* ─── Helpers ─────────────────────────────────────────────── */
function stackToList(s: StackBlock): string[] {
  return [s.frontend, s.backend, s.database, s.apis].filter(Boolean);
}

/* ─── File generators ─────────────────────────────────────── */

function genCursorRules(a: ValiSearchAnalysis): string {
  const techs = [...stackToList(a.tech_stack.mvp).slice(0, 4), ...stackToList(a.tech_stack.scalable).slice(0, 3)].join(", ");
  const features = a.product_strategy.mvp_features.map((f) => `  - ${f}`).join("\n");
  const buildSteps = a.build_mode.modules.slice(0, 6).map((s, i) => `  ${i + 1}. ${s}`).join("\n");

  return `# .cursorrules — ${a.branding.name_suggestions[0]}
# Generated by ValiSearch AI Startup Intelligence Platform

## Project Overview
${a.idea_analysis.summary}

## One-Liner
${a.idea_analysis.one_liner}

## Tech Stack
${techs}

## MVP Features (implement in order)
${features}

## Build Steps
${buildSteps}

## Code Style
- Prefer TypeScript strict mode
- Use functional components with hooks
- Colocate tests with source files
- No class components
- Prefer composition over inheritance
- Extract reusable logic into custom hooks
- Use named exports for components

## Architecture Rules
- Keep components under 200 lines — extract if larger
- Use ${a.tech_stack.mvp.backend} for backend API
- Validate all inputs with Zod schemas
- No hardcoded secrets — use environment variables only

## Naming Conventions
- Components: PascalCase
- Hooks: camelCase prefixed with "use"
- Files: kebab-case
- DB tables: snake_case

## AI Coding Instructions
When I ask you to build a feature:
1. Write the type definitions first
2. Implement the core logic
3. Write the UI component
4. Add error handling
5. Handle loading states

## Market
${a.validation.market_demand}

## Key Differentiators
${a.competitor_analysis.differentiation_strategy}
`;
}

function genVSCodeTasks(a: ValiSearchAnalysis): string {
  const tasks = a.kanban.backlog.slice(0, 6).map((task, i) => ({
    label: `${i + 1}. ${task.title}`,
    type: "shell",
    command: `echo "Starting: ${task.title.replace(/"/g, "'")} (${task.estimated_effort})"`,
    group: { kind: "build", isDefault: i === 0 },
    problemMatcher: [],
    detail: task.description,
  }));

  return JSON.stringify({ version: "2.0.0", tasks }, null, 2);
}

function genProjectMd(a: ValiSearchAnalysis): string {
  const mvpStack = stackToList(a.tech_stack.mvp).join(", ");
  const features = a.product_strategy.mvp_features.map((f) => `- ${f}`).join("\n");
  const competitors = a.competitor_analysis.competitors.map((c) => `- **${c.name}**: ${c.weaknesses[0]}`).join("\n");
  const allTasks = [...a.kanban.backlog, ...a.kanban.in_progress, ...a.kanban.completed]
    .slice(0, 10)
    .map((t) => `| ${t.title} | ${t.priority} | ${t.estimated_effort} |`)
    .join("\n");

  return `# ${a.branding.name_suggestions[0]} — Project Spec
> ${a.idea_analysis.summary}

## One-Liner
${a.idea_analysis.one_liner}

## Market Opportunity
${a.validation.market_demand}

## Solution
${a.competitor_analysis.differentiation_strategy}

## Tech Stack (MVP)
${mvpStack}

## MVP Features
${features}

## Sprint Plan
| Task | Priority | Effort |
|------|----------|--------|
${allTasks}

## Market
- TAM: ${a.market_research.tam_sam_som.tam}
- SAM: ${a.market_research.tam_sam_som.sam}
- SOM: ${a.market_research.tam_sam_som.som}

## Revenue
- Model: ${a.monetization.pricing_model}
- Streams: ${a.monetization.revenue_streams.join(", ")}

## Competitors
${competitors}

## Risks
${a.validation.risks.map((r) => `- ${r}`).join("\n")}

---
*Generated by ValiSearch — AI Startup Intelligence Platform*
`;
}

